/*>>>--CREACIÓN DE TABLAS Y RESTRICCIONES--<<<*/
--/1.TABLA EMPLEADO/--
CREATE TABLE EMPLEADO
( 
	ID_EMPLEADO				CHAR		(6) 	NOT NULL PRIMARY KEY,
	DNI						CHAR		(8) 	NOT NULL,
	EDAD					INT					NOT NULL,
	NOMBRE					VARCHAR		(20)	NOT NULL,
	APELLIDOS				VARCHAR		(30)	NOT NULL,
	TELEFONO				VARCHAR		(9)		NOT NULL
)
ALTER TABLE EMPLEADO	ADD CONSTRAINT UQ_DNI_EMP		UNIQUE	(DNI)
ALTER TABLE EMPLEADO	ADD CONSTRAINT UQ_TELEFONO_EMP	UNIQUE	(TELEFONO)
ALTER TABLE EMPLEADO	ADD CONSTRAINT CHK_EDAD			CHECK	(EDAD>=18)
GO


--/1.1. TABLA REPATIDOR/--
CREATE TABLE REPARTIDOR
(
	ID_EMPLEADO				CHAR		(6) 	NOT NULL PRIMARY KEY REFERENCES EMPLEADO,
	NUM_BREVETE				CHAR		(8)		NOT NULL
)
ALTER TABLE REPARTIDOR	ADD CONSTRAINT UQ_NUMBREVETE	UNIQUE	(NUM_BREVETE)
GO

--/1.2. TABLA EMPLEADO_COCINA/--
CREATE TABLE EMPLEADO_COCINA 
(
	ID_EMPLEADO				CHAR		(6) 	NOT NULL PRIMARY KEY REFERENCES EMPLEADO,
	CARGO					VARCHAR		(30)	NOT NULL
)
ALTER TABLE EMPLEADO_COCINA ADD CONSTRAINT CK_CARGO		CHECK	(CARGO IN ('MAS', 'AYU'))--replace with case
GO



--/2.TABLA HORNO/--
CREATE TABLE HORNO 
(
	ID_HORNO			CHAR		(6)		NOT NULL PRIMARY KEY,
	TIPO_HORNO			VARCHAR		(9)		NOT NULL,
	FECHA_COMPRA		DATE				NOT NULL
)
ALTER TABLE HORNO		ADD CONSTRAINT CK_TIPOHORNO		CHECK	(TIPO_HORNO IN ('ART', 'ELE', 'LEÑ'))--replace with case
GO

--/3.TABLA PIZZA/--
CREATE TABLE PIZZA 
(
	COD_PIZZA			CHAR		(5)		NOT NULL PRIMARY KEY,
	SABOR_PIZZA			VARCHAR		(28)	NOT NULL,
	PRECIO				MONEY				NOT NULL 
)
ALTER TABLE PIZZA		ADD CONSTRAINT CK_PRECIO		CHECK	(PRECIO>0)
GO

--/4.TABLA CLIENTE/--
CREATE TABLE CLIENTE 
( 
	ID_CLIENTE			CHAR		(6) 	NOT NULL PRIMARY KEY,
	DNI					CHAR		(8) 	NOT NULL,
	NOMBRE				VARCHAR		(20)	NOT NULL,
	APELLIDOS			VARCHAR		(30)	NOT NULL,
	DIRECCION			VARCHAR		(45)	NOT NULL,
	TELEFONO			VARCHAR		(9)		NOT NULL
)
ALTER TABLE CLIENTE		ADD CONSTRAINT UQ_DNI_CLI		UNIQUE (DNI)
ALTER TABLE CLIENTE		ADD CONSTRAINT UQ_TELEFONO_CLI	UNIQUE (TELEFONO)
GO

--/5.TABLA ORDEN/--
CREATE TABLE ORDEN 
(
	NUM_ORDEN				CHAR		(5)		NOT NULL PRIMARY KEY,
	ID_CLIENTE				CHAR		(6) 	NOT NULL REFERENCES CLIENTE,
	ID_EMPLEADO				CHAR		(6) 	NOT NULL,
	FECHA					DATE				NOT NULL
	FOREIGN KEY (ID_EMPLEADO) REFERENCES REPARTIDOR (ID_EMPLEADO)
)
GO


---/6.TABLA DETALLE ELABORACIÓN/---
CREATE TABLE ELABORACION 
(
	ID_EMPLEADO			CHAR		(6) 	NOT NULL,
	ID_HORNO			CHAR		(6)		NOT NULL REFERENCES HORNO,
	FECHA				DATE				NOT NULL
	FOREIGN KEY (ID_EMPLEADO) REFERENCES EMPLEADO_COCINA (ID_EMPLEADO),
	PRIMARY KEY (ID_EMPLEADO, ID_HORNO)
)
GO

---/7.TABLA DETALLE ELABORACIÓN_PIZZA/---
CREATE TABLE ELAB_PIZZA 
(
	ID_EMPLEADO			CHAR		(6) 	NOT NULL,
	ID_HORNO			CHAR		(6)		NOT NULL,
	COD_PIZZA			CHAR		(5)		NOT NULL REFERENCES PIZZA,
	CANTIDAD			INT					NOT NULL,
	FOREIGN KEY (ID_EMPLEADO, ID_HORNO) REFERENCES ELABORACION (ID_EMPLEADO, ID_HORNO),
	PRIMARY KEY (ID_EMPLEADO, ID_HORNO, COD_PIZZA)
)
ALTER TABLE ELAB_PIZZA		ADD CONSTRAINT CK_CANTIDAD_PRODUCE		CHECK	(CANTIDAD>0)
GO

---/8.TABLA DETALLE ORDEN_PIZZA/---
CREATE TABLE ORDEN_PIZZA 
(
	NUM_ORDEN			CHAR		(5)		NOT NULL REFERENCES ORDEN,
	COD_PIZZA			CHAR		(5)		NOT NULL REFERENCES PIZZA,
	CANTIDAD			INT	 	 	        NOT NULL,
	SUB_TOTAL			MONEY		 	    NOT NULL
	PRIMARY KEY (NUM_ORDEN, COD_PIZZA)
)
ALTER TABLE ORDEN_PIZZA		ADD CONSTRAINT CK_SUBTOTAL				CHECK	(SUB_TOTAL>0)
ALTER TABLE ORDEN_PIZZA		ADD CONSTRAINT CK_CANTIDAD_ORDEN_PIZZA	CHECK	(CANTIDAD>0)
GO




